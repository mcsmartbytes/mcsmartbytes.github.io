  static const Color rejectedColor = errorLight;
  static const Color draftColor = secondaryLight;

  /// Get status color based on status string
  static Color getStatusColor(String status) {
    switch (status.toLowerCase()) {
      case 'approved':
        return approvedColor;
      case 'pending':
        return pendingColor;
      case 'rejected':
        return rejectedColor;
      case 'draft':
        return draftColor;
      default:
        return secondaryLight;
    }
  }

